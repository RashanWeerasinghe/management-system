    public void loadToTable(){
        DefaultTableModel dtm= (DefaultTableModel)tbleTarget.getModel();
        dtm.setRowCount(0);
        List<TargetModel>list=TargetController.getAll();
        for(TargetModel target:list){
          Object[] row={target.getId(),target.getName(),target.getDate(),target.getAmount(),target.isCompleted()};
          dtm.addRow(row);
        }
    }
