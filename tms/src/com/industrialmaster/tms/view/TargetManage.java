    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //1. Get Data From form Fields
        String name = txtTargetName1.getText();
        String amount=txtTargetAmount.getText();
        String date=txtTargetDate.getText();

        TargetModel target=new TargetModel();
        target.setName(name);
        target.setAmount(Double.parseDouble(amount));
  
        try {
             SimpleDateFormat sdf=new SimpleDateFormat("YYYY-MM-DD");
             Date d=new Date(sdf.parse(date).getTime()); 
             target.setDate(d);
        } catch (Exception e) {
        }
     
       boolean done=TargetController.save(target);
       
        if (done) {
             //5.Display message to User
        JOptionPane.showMessageDialog(this, "Saved");
        }else{
            //5.Display message to User
        JOptionPane.showMessageDialog(this,"Not Saved");
        }
       
        
         
       
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        //1. Get Data From form Fields
        String name = txtId.getText();
        String amount=txtTargetAmount.getText();
        String date=txtTargetDate.getText();
        String id=txtId.getText();

        TargetModel target=new TargetModel();
        target.setId(Integer.parseInt(id));
        target.setName(name);
        target.setAmount(Double.parseDouble(amount));
  
        try {
             SimpleDateFormat sdf=new SimpleDateFormat("YYYY-MM-DD");
             Date d=new Date(sdf.parse(date).getTime()); 
             target.setDate(d);
        } catch (Exception e) {
        }
     
       boolean done=TargetController.update(target);
       
        if (done) {
             //5.Display message to User
        JOptionPane.showMessageDialog(this, "Updated");
        }else{
            //5.Display message to User
        JOptionPane.showMessageDialog(this,"Not Updated");
        }
        loadToTable();
    }//GEN-LAST:event_btnUpdateActionPerformed

